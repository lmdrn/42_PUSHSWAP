#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;
	int		s1_len;

	i = 0;
	s1_len = ft_strlen(s1);
	str = malloc((sizeof(*str) * s1_len) + 1);
	if (str == NULL)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
