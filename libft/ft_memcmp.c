#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t nbytes)
{
	size_t	i;

	i = 0;
	while (nbytes > 0)
	{
		if (((unsigned char *)s1)[i] != ((unsigned char *)s2)[i])
			return (((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]);
		else
		{
			i++;
		}
		nbytes --;
	}
	return (0);
}
